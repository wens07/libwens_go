package util_lib

// reverse slice
func ReverseSlice(slice []byte) []byte {

	for i := 0; i < len(slice)/2; i++ {
		slice[i], slice[len(slice)-1-i] = slice[len(slice)-1-i], slice[i]
	}

	return slice
}
